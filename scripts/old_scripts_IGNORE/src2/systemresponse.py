# Brief: This class defines the metaclass of SystemResponse in order to implement Singleton Pattern
class SystemResponseMeta(type):
    _instances = {}
    def __call__(cls, *args, **kwargs):
        if cls not in cls._instances:
            instance = super().__call__(*args, **kwargs)
            cls._instances[cls] = instance
        return cls._instances[cls]
        

# Brief: This class is responsible to store all possible outputs from the execution of the experiment 
class SystemResponse(metaclass=SystemResponseMeta):
    def __init__(self):
        self.response = []

